#include <iostream>
using namespace std;
// {i.s : Program Menunggu Masukan Angka}
// {f.s : Program Menampilkan Angka}
main () {
    // kamus
    int a = 21;
    int b = 10;
    int c ;

    c = a + b ;
    cout << c << endl ;

    c = a - b ;
    cout << c << endl ;

    c = a * b ;
    cout << c << endl ;

    c = a / b ;
    cout << c << endl ;

    c = a % b ;
    cout << c << endl ;

    c = a ++;
    cout << c << endl ;

    c = a --;
    cout << c << endl ;

    return 0;
}
